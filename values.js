// javascript follows order of operations: PEMDAS

// ex:

(5 + 7 ) * 3  // 12 * 3 = 36

(3 * 4) + 3 - 12 / 2
// 12 + 3 - 12 / 2
// 12 + 3 - 6
// 9

(-5 * 6) - 7 * -2
// -30 - 7 * -2
// -30 - -14
// -16

// Modules example: Modules follows same rule set as "M" or Multiplication in PEMDAS
4 + (8 % (3 + 1))
// 4 + (8 % 4)
// 4 + 0
// 4

